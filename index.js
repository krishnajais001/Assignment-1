let fruits = []; // Creating An Empty Array
fruits = ["Apple", "Banana", "Orange"]; // Adding Fruits to the array
fruits.shift(); // Remove the Frist Element
fruits.push("Grape"); // Adding Fruits to the end
fruits.splice(1, 1, "Pear"); // Replace the pear to orange
